    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        listaEmprestimos.setModel(modeloLista);
        modeloLista.clear();
        if (!txtLoginId.getText().isEmpty()){
            emprestimosEncontrados = buscaEmprestimos(txtLoginId.getText()); 
            
            if (!emprestimosEncontrados.isEmpty()){
                Iterator it = emprestimosEncontrados.iterator();
                String [] emprestimo;
                while (it.hasNext()){
                   emprestimo = (String []) it.next();
                   String  codigoDoEmprestimo = geraCodigoDeEmprestimo (emprestimo[4]);
                   String [] dataDividida = emprestimo[4].split(" ");
                   String hora = dataDividida[1];
                   dataDividida = dataDividida[0].split("-");
                   
                   
                   modeloLista.addElement(emprestimo[0] + "        " + dataDividida[2] + "/" + dataDividida[1] + "/" + dataDividida[0] + " às" + hora + "        " + codigoDoEmprestimo );  
                }
            }else{
                JOptionPane.showMessageDialog(null, "O Usuário não possui empréstimos ou não está cadastrado!");  
            }
         }else{
            JOptionPane.showMessageDialog(null, "Preencha o campo do ID!");   
         }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            constroiListaDeEmprestimos();
        } catch (ParseException ex) {
            Logger.getLogger(TelaConsultaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (this.loginAlunoProfessor != null){
           btnConsultar.setVisible(false);
           txtLoginId.setText(loginAlunoProfessor);
           txtLoginId.setEditable(false);
           listaEmprestimos.setModel(modeloLista);
           modeloLista.clear();
           if (!txtLoginId.getText().isEmpty()){
            emprestimosEncontrados = buscaEmprestimos(txtLoginId.getText());
            if (!emprestimosEncontrados.isEmpty()){
                Iterator it = emprestimosEncontrados.iterator();
                String [] emprestimo;
                
                while (it.hasNext()){
                   emprestimo = (String []) it.next();
                   String  codigoDoEmprestimo = geraCodigoDeEmprestimo (emprestimo[4]);
                   String [] dataDividida = emprestimo[4].split(" ");
                   String hora = dataDividida[1];
                   dataDividida = dataDividida[0].split("-");
                   
                   
                   modeloLista.addElement(emprestimo[0] + "         " + dataDividida[2] + "/" + dataDividida[1] + "/" + dataDividida[0] + " às" + hora + "         " + codigoDoEmprestimo );  
                }
            }else{
                JOptionPane.showMessageDialog(null, "O Usuário não possui empréstimos ou não está cadastrado!");  
            }
         }else{
            JOptionPane.showMessageDialog(null, "Preencha o campo do ID!");   
         }
        }
    }//GEN-LAST:event_formWindowOpened

