    private void listaEmprestimosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaEmprestimosMouseClicked
        if (!modeloLista.isEmpty()){
            String codigoDoEmprestimo;
            int indiceDoElementoSelecionado = listaEmprestimos.getSelectedIndex();
            String[] emprestimoSelecionado = emprestimosEncontrados.get(indiceDoElementoSelecionado);
            String [] obraDoEmprestimo = buscaObra( Integer.parseInt(emprestimoSelecionado[3]));
            codigoDoEmprestimo = geraCodigoDeEmprestimo (emprestimoSelecionado[4]);
            
            if (obraDoEmprestimo != null){
                lblTituloDaObraValor.setText(obraDoEmprestimo[1]);
            }
            lblTituloEmprestimo.setText(codigoDoEmprestimo);
            lblFuncionarioValor.setText(emprestimoSelecionado[0]);
            lblObraValor.setText(emprestimoSelecionado[3]);
            lblExemplarValor.setText(emprestimoSelecionado[2]);
            String [] dataDividida = emprestimoSelecionado[5].split(" ");
            dataDividida = dataDividida[0].split("-");
            lblDataDeDevolucaoValor.setText(dataDividida[2] + "/" + dataDividida[1] + "/" + dataDividida[0]);
        }
    }//GEN-LAST:event_listaEmprestimosMouseClicked

