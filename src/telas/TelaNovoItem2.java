    private int geraCodigo (){ //com 9 dígitos
        Random gerador = new Random ();
        int codigo = 100000000 + gerador.nextInt(999999999);
        return codigo;
    }
    private String [] buscaExemplar(int codigoDoExemplar, int codigoDaObra){
        String [] resposta = null;
        try {
            resposta = CExemplar.search(codigoDoExemplar, codigoDaObra);
        }catch (SQLException e) {
            System.out.println("Erro!" + e);
            return resposta;
        }
        return resposta;
    }
    private int adicionaExemplar (int codigoDaObra){
        int novoCodigo;
        do{
           novoCodigo = geraCodigo();
        } while(buscaExemplar(novoCodigo, codigoDaObra)!= null);
        try {
            CExemplar.create(novoCodigo, codigoDaObra, false);
        }catch (SQLException e) {
            System.out.println("Erro!" + e);
            return novoCodigo;
        }
        return novoCodigo;
        
    }
    
    private String [] buscaObra(int codigoDaObra){
        String [] resposta = null;
        try {
            resposta = CObra.search(codigoDaObra);
        }catch (SQLException e) {
            System.out.println("Erro!" + e);
            return resposta;
        }
        return resposta;
    }
    
    private int adicionaObra (String titulo, String categoria, String autor, String dataDePublicacao, int codEdit){
        int novoCodigo;
        do{
           novoCodigo = geraCodigo();
        } while(buscaObra (novoCodigo)!= null);
        try {
            CObra.create(novoCodigo, titulo, categoria, autor, dataDePublicacao, codEdit);
        }catch (SQLException e) {
            System.out.println("Erro!" + e);
            return 0;
        }
        return novoCodigo;
    }
    
    private void constroiComboEditora(){
        ArrayList<String[]> editoras = new ArrayList();
        try {
            editoras = (ArrayList<String[]>)CEditora.read();
            }catch (SQLException e) {
                System.out.println("Erro!" + e);
        }  
        Iterator it = editoras.iterator();
        
        while (it.hasNext()){
            String [] editora = (String[]) it.next();
            comboEditora.addItem(editora[1]);
        }
        comboEditora.setSelectedIndex(3);
        
    }
    
    public String toTitledCase(String str){

    String[] words = str.split("\\s");
    StringBuilder sb = new StringBuilder();

    for(int i = 0; i < words.length; i++){
        sb.append(words[i].substring(0, 1).toUpperCase() + words[i].substring(1).toLowerCase());
        sb.append(" ");
    }

    return sb.toString();
}
    
