    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String titulo = txtTitulo.getText();
        String categoria = toTitledCase(comboCategoria.getSelectedItem().toString());
        String autor = txtAutor.getText();
        int editora = comboEditora.getSelectedIndex()+1; 
        String data = txtData.getText();
        
        if (!titulo.isEmpty() && !autor.isEmpty() && !data.equals("  /  /    ") ){
        titulo = toTitledCase(txtTitulo.getText());
        autor = toTitledCase(txtAutor.getText());
        data = txtData.getText();
        String[] diaMesAno = data.split("/");
        data = diaMesAno[2] + "/" + diaMesAno[1] + "/" + diaMesAno[0];
        
            int codigoDaNovaObra = adicionaObra (titulo, categoria, autor, data, editora); //CORRIGIR editora!!!!!!!
            if (codigoDaNovaObra != 0){
                JOptionPane.showMessageDialog(null, "Obra cadastrada com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null, "Ocorreu um erro! Tente novamente.");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

