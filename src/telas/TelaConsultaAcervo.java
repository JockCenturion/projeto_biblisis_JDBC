    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        modeloDeListaDeAcervo.clear();
        String titulo = txtTitulo.getText();
        if (titulo.length() >= 2){
            ArrayList<String> resultadoDaBusca = null;
            int indice;
            if (!titulo.equals("")){
                resultadoDaBusca = buscaSubstring(titulo);  
            }
            if (resultadoDaBusca != null){
                Iterator it = resultadoDaBusca.iterator();
           while(it.hasNext()){
               modeloDeListaDeAcervo.addElement(it.next().toString());
           }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Digite pelo menos 2 caracteres para a busca!"); 
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

