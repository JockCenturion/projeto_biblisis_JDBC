    private ArrayList<String> buscaSubstring (String substring){
        Iterator it = this.acervo.iterator();
        String [] obra;
        ArrayList <String> resposta = new ArrayList() ;
        while (it.hasNext()){
            obra = (String [])it.next();
            if (obra[1].toLowerCase().contains(substring.toLowerCase())){
                resposta.add(obra [1]);
                //System.out.println(resposta[indice]);
                
            }
        }
        return resposta;
    }
    
    private String [] buscaObra (String tituloDaObra){
        Iterator it = this.acervo.iterator();
        String [] obra;
        while (it.hasNext()){
            obra = (String [])it.next();
            if (obra[1].equals(tituloDaObra) ){
                return obra;
            }
        }
        return null;
    }

